#!/usr/bin/env node
// build-ledger.mjs — assemble the LEAPFROG ledger for READING.
//
// The append-only ledger USED to be one file (LEAPFROG.md) that every parallel chat
// appended to at EOF with a hand-picked sequential number (L257…) — two chats would
// pick the same number and collide, and appends at the same line always conflict.
//
// NEW model (conflict-free by construction):
//   • LEAPFROG.md      — the FROZEN historical archive + the studio playbook/RULE.
//   • leapfrog/lessons/*.md — one file PER new lesson, named `YYYY-MM-DD-slug.md`.
//     Two chats never touch the same file, so two lessons never conflict. No numbers.
//
// This script just CONCATENATES the archive + the dated fragments (sorted) into a
// single `leapfrog/LEDGER.md` for easy reading. It is DERIVED (git-ignored) — the
// fragments + LEAPFROG.md are the source of truth, so you never HAVE to run it and it
// can never cause a merge conflict. Run it whenever you want the whole thing in one
// scroll:  `node tools/build-ledger.mjs`
import { readFileSync, writeFileSync, readdirSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const ARCHIVE = join(ROOT, 'LEAPFROG.md');
const LESSONS_DIR = join(ROOT, 'leapfrog', 'lessons');
const OUT = join(ROOT, 'leapfrog', 'LEDGER.md');

const archive = existsSync(ARCHIVE) ? readFileSync(ARCHIVE, 'utf8') : '';

let fragments = [];
if (existsSync(LESSONS_DIR)) {
  fragments = readdirSync(LESSONS_DIR)
    .filter((f) => f.endsWith('.md') && f.toLowerCase() !== 'readme.md')
    .sort();   // `YYYY-MM-DD-slug.md` sorts chronologically by name
}

const parts = [archive.trimEnd()];
if (fragments.length) {
  parts.push('\n\n---\n\n# 🆕 Lessons since the archive freeze (from `leapfrog/lessons/`)\n');
  parts.push('_Generated by `tools/build-ledger.mjs` — do not edit here; edit the fragment files._\n');
  for (const f of fragments) {
    parts.push('\n' + readFileSync(join(LESSONS_DIR, f), 'utf8').trim() + '\n');
  }
}

writeFileSync(OUT, parts.join('\n') + '\n');
console.log(`wrote ${OUT}  (archive + ${fragments.length} fragment${fragments.length === 1 ? '' : 's'})`);
